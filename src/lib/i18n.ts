const DICTS: Record<Lang, Dict> = { en, rw, fr };
let current: Lang = "en";
const listeners = new Set<() => void>();

export function initLang() {
  if (typeof window === "undefined") return;
  const stored = localStorage.getItem(KEY) as Lang | null;
  if (stored && DICTS[stored]) current = stored;
}

export function setLang(l: Lang) {
  current = l;
  if (typeof window !== "undefined") localStorage.setItem(KEY, l);
  listeners.forEach((cb) => cb());
}

export function getLang(): Lang { return current; }
export function t(key: string): string { return DICTS[current][key] ?? DICTS.en[key] ?? key; }

export function useLang(): Lang {
  return useSyncExternalStore(
    (cb) => { listeners.add(cb); return () => { listeners.delete(cb); }; },
    () => current,
    () => "en",
  );
}

export function useT() { useLang(); return t; }

